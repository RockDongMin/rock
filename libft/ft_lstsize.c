#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		n;
	t_list	*n_lst;

	n = 0;
	n_lst = lst;
	while (n_lst)
	{
		n_lst = n_lst->next;
		n++;
	}
	return (n);
}
