void	ft_rev_int_tab(int *tab, int size)
{
	int	cp;
	int	i;
	int	j;

	i = size - 1;
	j = 0;
	while (i > j)
	{
		cp = tab[i];
		tab[i] = tab[j];
		tab[j] = cp;
		i--;
		j++;
	}
}
