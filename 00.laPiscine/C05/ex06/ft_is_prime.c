int	ft_is_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	i = nb - 1;
	while (i > 1)
	{
		if (!(nb % i))
			return (0);
		i--;
	}
	return (1);
}
